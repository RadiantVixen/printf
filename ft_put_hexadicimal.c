#include"printf.h"

void	ft_put_hexadicimal(int n)
{
	int tab[100];
	int	i;
	i = 0;
	while (n >= 1)
	{
		tab[i] = n % 16;
		n = n / 16;
		i++;
	}
	i--;
	while(i >= 0)
	{
		if (tab[i] >= 10)
		{
			ft_putchar('a' + tab[i] - 10);
			i--;
		}
		ft_putnbr(tab[i]);
		i--;
	}	
}

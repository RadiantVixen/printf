#include "printf.h"

void	ft_put_binaire(int n)
{
	int tab[100];
	int	i;
	i = 0;
	while (n >= 1)
	{
		tab[i] = n % 2;
		n = n / 2;
		i++;
	}
	while(i >= 0)
	{
		ft_putnbr(tab[i]);
		i--;
	}	
}
#include "printf.h"

void	ft_putnbr_ns(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n >= 0 && n <= 9)
	{
		ft_putchar (n + '0');
	}
	else
	{
		ft_putnbr_ns(n / 10);
		ft_putnbr_ns(n % 10);
	}
}